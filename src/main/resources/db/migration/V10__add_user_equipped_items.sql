    ADD CONSTRAINT fk_user_equipped_items_on_fk_user
        FOREIGN KEY (fk_user_id) REFERENCES users (id);

ALTER TABLE user_equipped_items
    ADD CONSTRAINT fk_user_equipped_items_on_fk_product
        FOREIGN KEY (fk_product_id) REFERENCES products (id);

CREATE INDEX idx_user_equipped_items_user_id
    ON user_equipped_items (fk_user_id);
